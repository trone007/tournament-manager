<?php
namespace Src\Model;

use Doctrine\ORM\Annotation as ORM;

/**
 * @ORM\Entity(repositoryClass="TeamRepository")
 * @ORM\Table(name="team")
 **/
class Team implements EntityInterface
{
    /**
     * @ORM\Id
     * @ORM\Column(type="integer")
     * @ORM\GeneratedValue
     * @var int
     */
    private $id;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @ORM\Column(type="string")
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name)
    {
        $this->name = $name;
    }



}